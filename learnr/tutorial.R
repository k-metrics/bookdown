##### Variables ###############################################################

## ---- vector-1 --------------------------------------------------------------
x <- 2L                   # 一つだけ代入（格納）する場合 `c` は省略可能です
str(x)                    # 値が一つだけの場合は長さ表示が省略されます


## ---- vector-2 --------------------------------------------------------------
x <- c(1, 2, 3)           # 実数の 1 から 3 の三つの値が代入されます
str(x)                    # []の部分が長さ表示です


## ---- vector-3 --------------------------------------------------------------
x <- c("1", '2', "3")     # 文字として数字を代入してみます
str(x)


## ---- vector-4 --------------------------------------------------------------
x <- c(1L, 2, "3")        # 整数、実数、文字としての数字を代入してみます
str(x)                    # 強制型変換により最も柔軟度の高い文字型になります


## ---- factor ----------------------------------------------------------------
x <- factor(c("A", "B", "AB", "O", "A", "A", "A", "B"))
str(x)
levels(x)


## ---- ordered ---------------------------------------------------------------
x <- ordered(c("A", "B", "AB", "O", "A", "A", "A", "B"))
str(x)
levels(x)


## ---- matrix-1 --------------------------------------------------------------
x <- matrix(c(1, 2, 3, 4, 5, 6), nrow = 2, ncol = 3)
str(x)
x


## ---- matrix-2 --------------------------------------------------------------
x <- matrix(c(1, 2, 3, 4, 5, 6), nrow = 2, ncol = 3, byrow = TRUE)
str(x)
x


## ---- matrix-3 --------------------------------------------------------------
x <- matrix(c("1", "2", "3", "4", "5", "6"), nrow = 3)
str(x)
x


## ---- array -----------------------------------------------------------------
x <- array(1:10, dim = c(2, 3, 2))
str(x)
x


## ---- dataframe -------------------------------------------------------------
x <- data.frame(col1 = c(1:5), col2 = c("A", "B", "C", "D", "E"),
                col3 = c(10, 11, 12, 13, 14), col4 = c(TRUE, TRUE, FALSE, TRUE, FALSE))
str(x)
x


## ---- list ------------------------------------------------------------------
x <- list(c(1:10), c(0.5:5.5), seq(1, 4, 0.2), c("A", "B", "AB", "O"), x)
str(x)
x


